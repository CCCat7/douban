package doubandao;

public class Performer {
	private String Performer_ID;
	private String Performer_Type;
	private String Performer_Name;
	private String Performer_Sex;
	private String Performer_Birth;
	public String getPerformer_ID() {
		return Performer_ID;
	}
	public void setPerformer_ID(String performer_ID) {
		Performer_ID = performer_ID;
	}
	public String getPerformer_Type() {
		return Performer_Type;
	}
	public void setPerformer_Type(String performer_Type) {
		Performer_Type = performer_Type;
	}
	public String getPerformer_Name() {
		return Performer_Name;
	}
	public void setPerformer_Name(String performer_Name) {
		Performer_Name = performer_Name;
	}
	public String getPerformer_Sex() {
		return Performer_Sex;
	}
	public void setPerformer_Sex(String performer_Sex) {
		Performer_Sex = performer_Sex;
	}
	public String getPerformer_Birth() {
		return Performer_Birth;
	}
	public void setPerformer_Birth(String performer_Birth) {
		Performer_Birth = performer_Birth;
	}
}